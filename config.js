/* ================================================================
   游戏数字配置区
   以后要调整奖金金额、淘汰轮次、银行家出价比例，只需要改这个文件，
   不需要碰 index.html 里的其他代码。改完保存，刷新页面即可生效。
   ================================================================

   使用说明：

   1. currency
      货币符号，显示在每个金额前面，例如 'RM'、'¥'、'$' 都可以。

   2. useMillionPrizes（重要！千元档 / 百万档 切换开关）
      true  → 使用下面的 prizesMillion（百万级奖金，最高 100 万）
      false → 使用下面的 prizesThousand（千元级奖金，最高 1000）
      只改这一个 true/false，不用动数组本身。

   3. prizesThousand / prizesMillion
      两套 24 个红包对应的奖金金额，数组顺序不影响游戏（开局会自动打乱），
      但建议保持"从小到大"排列，方便你自己看着改数值。
      —— 每套都必须是 24 个数字，多一个少一个都会导致红包数量对不上。
      —— 想改具体金额，直接改对应数组里的数字即可。

   4. roundPlan
      每一轮需要淘汰的红包数量，例如 [6,5,4,3,2,1,1] 表示：
      第一轮淘汰6个、第二轮淘汰5个……最后两轮各淘汰1个。
      —— 这些数字加起来必须等于 22
         （24个红包 − 1个玩家自己选的幸运红包 − 1个留到终局的红包 = 22）
      —— 数组有几项，游戏就有几轮银行家报价，offerPercents 也要对应改成一样的项数。

   5. offerPercents
      每一轮银行家出价的比例，对应 roundPlan 同一轮次。
      数值是"银行家出价 ÷ 剩余红包平均值"，例如 0.15 表示银行家只愿意出剩余红包平均值的15%，
      数值越接近1，银行家的报价就越接近真实平均值（对玩家越有利）。
      —— 建议整体递增（越到后面报价比例越高），符合真人秀节目的节奏感。
   ================================================================ */

// true = 用百万档奖金（prizesMillion）；false = 用千元档奖金（prizesThousand）
const useMillionPrizes = true;

const prizesThousand = [
  0.10, 0.50, 1, 5, 10, 20, 30, 50, 75, 100, 150, 200,
  250, 300, 350, 400, 500, 600, 700, 750, 800, 900, 950, 1000
];

const prizesMillion = [
  0.10, 1, 100, 500, 1000, 2000, 3000, 5000, 7500, 10000, 15000, 20000,
  250000, 300000, 350000, 400000, 500000, 600000, 700000, 750000, 800000, 900000, 950000, 1000000
];

const CONFIG = {
  currency: 'RM',
  prizes: useMillionPrizes ? prizesMillion : prizesThousand,
  roundPlan: [6, 5, 4, 3, 2, 1, 1],
  offerPercents: [0.15, 0.30, 0.45, 0.55, 0.65, 0.75, 0.90]
};
